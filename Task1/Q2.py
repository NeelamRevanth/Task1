#Define Python variable, with your won Example? 

#A Python variable is a name that refers to a value stored in memory.
#It acts like a container that holds data, and you can use that name to access or change the value later.

#Example

# Assigning values to variables
name = "Revanth"       # String variable
age = 23               # Integer variable
is_developer = True    # Boolean variable

# Printing variables
print("Name:", name)
print("Age:", age)
print("Developer:", is_developer)
